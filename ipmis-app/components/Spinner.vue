<template>
    <div v-if="show" v-bind="$attrs" class="flex items-center justify-center" :class="sizeClasses" role="status">
        <svg class="animate-spin text-primary" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
            <circle class="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" stroke-width="4" />
            <path class="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8v4a4 4 0 00-4 4H4z" />
        </svg>
        <span v-if="label" class="ml-2 text-sm text-gray-600">
            {{ label }}
        </span>
    </div>
</template>

<script setup>
const props = defineProps({
    show: { type: Boolean, default: false },
    size: { type: String, default: "md" },
    label: { type: String, default: "" },
})

const sizeClasses = computed(() => {
    switch (props.size) {
        case "sm":
            return "h-4 w-4"
        case "lg":
            return "h-10 w-10"
        default:
            return "h-6 w-6"
    }
})
</script>
